package day05_Concatenation.day06_PrimitiveTypeCasting;

public class PrimitiveCasting {
    public static void main(String[] args) {

        byte a =10;
        short b = (short)a;
        System.out.println("b = " + b);
        long c = -410000000;
        byte d = (byte)c;
        System.out.println("d = " + d);
        double l = 5.3;
        float m = (float)l;
        System.out.println("l = " + m);

        double h = 3.14;
        short PI = (short) h;





    }




}
