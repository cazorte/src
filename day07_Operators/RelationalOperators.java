package day07_Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        // == !=

        int x=15, y=30;

        boolean result1 = x<y;
        System.out.println("result1 = " + result1);
        boolean result2 = x==y;
        System.out.println("result2 = " + result2);
        boolean result3 = x!=y;
        System.out.println("result3 = " + result3);










    }





}
